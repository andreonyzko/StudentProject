    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        boolean ok;
        try{
            AlunoControle alunocontrole = new AlunoControle();
            ok = alunocontrole.cadastrar_aluno(txtNome.getText(), txtCpf.getText(), txtEmail.getText(), txtFone.getText());
            if(ok==true){
                limpar();
            }
            else{
                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Aviso"+ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

