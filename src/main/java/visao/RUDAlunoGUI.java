    private void listar_dados(){
        AlunoControle alunocontrole = new AlunoControle();
        DefaultTableModel modelo = (DefaultTableModel) tblAlunos.getModel();
        modelo.setRowCount(0);
        for(Aluno aluno: alunocontrole.recuperar_aluno()){
            modelo.addRow(new Object[]{aluno.getCodigo(),aluno.getNome(),aluno.getCpf(),aluno.getEmail(),aluno.getFone()});
        }
    }
    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        listar_dados();
    }//GEN-LAST:event_btnListarActionPerformed

