    private void tblAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlunosMouseClicked
        if(tblAlunos.getSelectedRow() != -1){
            txtCodigo.setText(tblAlunos.getValueAt(tblAlunos.getSelectedRow(),0).toString());
            txtNome.setText(tblAlunos.getValueAt(tblAlunos.getSelectedRow(),1).toString());
            txtCpf.setText(tblAlunos.getValueAt(tblAlunos.getSelectedRow(),2).toString());
            txtEmail.setText(tblAlunos.getValueAt(tblAlunos.getSelectedRow(),3).toString());
            txtFone.setText(tblAlunos.getValueAt(tblAlunos.getSelectedRow(),4).toString());
        }
        else{
            JOptionPane.showMessageDialog(null, "Nenhuma linha foi selecionada","Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblAlunosMouseClicked

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        if(tblAlunos.getSelectedRow() != 1){
            AlunoControle alunocontrole = new AlunoControle();
            alunocontrole.atualizar_aluno(txtCodigo.getText(),txtNome.getText(),txtCpf.getText(),txtEmail.getText(),txtFone.getText());
            listar_dados();
            JOptionPane.showMessageDialog(null, "Atualizado com Sucesso","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada","Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if(tblAlunos.getSelectedRow() != -1){
            AlunoControle alunocontrole = new AlunoControle();
            alunocontrole.excluir_aluno(txtCodigo.getText(),txtNome.getText(),txtCpf.getText(),txtEmail.getText(),txtFone.getText());
            listar_dados();
            JOptionPane.showMessageDialog(null, "Excluido com Sucesso","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada","Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

